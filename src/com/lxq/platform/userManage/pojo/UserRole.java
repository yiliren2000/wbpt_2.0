package com.lxq.platform.userManage.pojo;


/**
 * 用户角色权限类
 * @author lixueqing
 *
 */
public class UserRole {
	
	/**权限主键*/
	private int user;

	/**角色主键*/
	private int role;

	public int getUser() {
		return user;
	}

	public void setUser(int user) {
		this.user = user;
	}

	public int getRole() {
		return role;
	}

	public void setRole(int role) {
		this.role = role;
	}

	
}